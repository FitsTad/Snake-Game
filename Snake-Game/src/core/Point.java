package core;

/**
 *
 * @author Alex Liao
 * 
 * Core utility class for storing a coordinate pair
 */
public class Point {
    /**
     * The coordinates
     */
    public final int x, y;
    
    /**
     * Construct point (x, y)
     * 
     * @param x x
     * @param y y
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
